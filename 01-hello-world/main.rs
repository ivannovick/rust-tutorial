// Lesson 01 — Hello World and `main`
//
// `fn main()` is the entry point of every Rust binary. When the program
// starts, the operating system calls this function first.
fn main() {
    // `println!` is a macro (note the `!`) that prints a line to stdout.
    println!("Airline Management System starting...");
    println!("Execution begins in main()");
}
